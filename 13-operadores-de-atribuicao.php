<?php
    // Operadores de atribuição

    echo "*** Operadores de atribuição *** <br><br>";

    $a = 10;
    $b = 5;

    $a += $b; // $a = $a + $b; 
    echo $a;
    echo "<br>";

    $a -= $b; // $a = $a - $b;
    echo $a;
    echo "<br>";

    $a *= $b; // $a = $a * $b;
    echo $a;
    echo "<br>";

    $a /= $b; // $a = $a / $b;
    echo $a;
    echo "<br>";

    $a %= $b; // $a = $a % $b;
    echo $a;